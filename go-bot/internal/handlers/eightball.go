func (b *Bot) handleEightBall(c tele.Context) error {
	question := c.Message().Payload
	if question == "" {
		return c.Reply("🎱 Постав питання: /8ball чи буде деплой сьогодні?")
	}

	answer := eightBallAnswers[rand.Intn(len(eightBallAnswers))]
	return c.Reply(fmt.Sprintf("🎱 *%s*\n\n%s", question, answer), &tele.SendOptions{ParseMode: tele.ModeMarkdown})
}
